/**
 * @author japarejo
 */
@Configuration
@EnableWebSecurity
public class SecurityConfiguration extends WebSecurityConfigurerAdapter {

	@Autowired(required = false)
	private JwtRequestFilter jwtRequestFilter;

	
	@Autowired
	DataSource dataSource;
	
	@Override
	protected void configure(HttpSecurity http) throws Exception {
		http.authorizeRequests()
				.antMatchers("/resources/**","/webjars/**","/h2-console/**").permitAll()
				.antMatchers(HttpMethod.GET, "/","/oups").permitAll()				
				.antMatchers("/authenticate","/login").permitAll()
				.antMatchers("/api/**").hasAnyAuthority("admin")
				.antMatchers("/swagger-ui.html","/v3/**","/swagger-ui/**").permitAll()
				.antMatchers("/users/new").permitAll()				
				.antMatchers("/logging", "/actuator/**").permitAll()
				.antMatchers("/admin/**").hasAnyAuthority("admin")
				.antMatchers("/owners/**").hasAnyAuthority("owner","admin")				
				.antMatchers("/vets/**").authenticated()
				.antMatchers("/payments/**").authenticated()
				.antMatchers("/bills/**").authenticated()
				.anyRequest().denyAll()
				.and()
				 	.formLogin()				 	
				 	.failureUrl("/login-error")
				.and()
					.logout()
						.logoutSuccessUrl("/"); 
                // Configuraci칩n para que funcione la consola de administraci칩n 
                // de la BD H2 (deshabilitar las cabeceras de protecci칩n contra
                // ataques de tipo csrf y habilitar los framesets si su contenido
                // se sirve desde esta misma p치gina.
                
                http.headers().frameOptions().sameOrigin();
                if(jwtRequestFilter!=null)
                	http.addFilterBefore(jwtRequestFilter, UsernamePasswordAuthenticationFilter.class);
				http.csrf().disable();
				//http.exceptionHandling().authenticationEntryPoint(new Http403ForbiddenEntryPoint());
	}

	@Override
	public void configure(AuthenticationManagerBuilder auth) throws Exception {
		auth.jdbcAuthentication()
	      .dataSource(dataSource)
	      .usersByUsernameQuery(
	       "select username,password,enabled "
	        + "from users "
	        + "where username = ?")
	      .authoritiesByUsernameQuery(
	       "select username, authority "
	        + "from authorities "
	        + "where username = ?")	      	      
	      .passwordEncoder(passwordEncoder());	
	}
	
	@Bean
	public PasswordEncoder passwordEncoder() {	    
		PasswordEncoder encoder =  NoOpPasswordEncoder.getInstance();
	    return encoder;
	}
	
	
	@Bean
	@Override
	public AuthenticationManager authenticationManagerBean() throws Exception {
		return super.authenticationManagerBean();
	}
}


